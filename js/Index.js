const firstNumber = Number(prompt('Enter the first number.'));
const secondNumber = Number(prompt('Enter the second number.'));

alert('sum: ' + firstNumber + ' + ' + secondNumber + ' = ' + (firstNumber + secondNumber));
alert('subtraction: ' + firstNumber + ' - ' + secondNumber + ' = ' + (firstNumber - secondNumber));
alert('Multiplication: ' + firstNumber + ' * ' + secondNumber + ' = ' + (firstNumber * secondNumber));
alert('division: ' + firstNumber + ' / ' + secondNumber + ' = ' + (firstNumber / secondNumber));